# helper functions for geneartion of github-action-warnings
function recursive_second(vec)
    s = []
    for e in vec
        if typeof(e[2]) == String
            push!(s, e[2])
        else
            append!(s, recursive_second(e[2]))
        end
    end
    return s
end
function recursive_second_filter!(f, a)
    deleteat = []
    for i in keys(a)
        if typeof(a[i][2]) == String
            if f(a[i]) == false
                push!(deleteat, i)
            end
        else
            recursive_second_filter!(f, a[i][2])
            if length(a[i][2]) == 0
                push!(deleteat, i)
            end
        end
    end
    deleteat!(a, deleteat)
    return a
end

